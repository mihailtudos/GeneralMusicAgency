    public void displayData(String userName){
        String sql = "SELECT * FROM users WHERE user_name=?";
        try{
            prepStatement = connection.prepareStatement(sql);
            prepStatement.setString(1, userName);
            result = prepStatement.executeQuery();
    
            if(result.next()){
                userFirstNameDisplay.setText(result.getString("first_name"));
                userSecondNameDisplay.setText(result.getString("second_name"));
                address.setText(result.getString("address_1") + ", " + result.getString("post_code"));
                email.setText(result.getString("email"));
                phoneNo.setText(result.getString("phone_no"));
                userTitle.setText(result.getString("title"));
                userAddress.setText(result.getString("address_1"));
                userSecondAddress.setText(result.getString("address_2"));
                userTown.setText(result.getString("town"));
                userPostCode.setText(result.getString("post_code"));
                userEmail.setText(result.getString("email"));
                userPhone.setText(result.getString("phone_no"));
                userPassword.setText("");
                userConfPassword.setText("");
                userSecCode.setText(result.getString("security_code"));
                if(result.getString("user_level").equals("user")){
                    
                    orgData.setVisible(false);
                }else{
                    orgType.setText(result.getString("org_type"));
                    orgAccountNo.setText(result.getString("account_no"));
                    orgPostCode.setText(result.getString("account_postcode"));
                    orgName.setText(result.getString("org_name"));
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "User Not Found");
                result.close();
                prepStatement.close();
                connection.close();
                goToDashboard();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
           
        }        
    }
