    private void addAgentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAgentActionPerformed
        // TODO add your handling code here:
        String addAgentSQL = "INSERT INTO `agents` (`first_name`, `second_name`, `phone_no`, `email`, `address`, `post_code`, `date_of_birth`) VALUES (?,?,?,?,?,?,?);";
        try{
            prepStatementForAgent = connection.prepareCall(addAgentSQL);
            prepStatementForAgent.setString(1, firstName.getText());
            prepStatementForAgent.setString(2, secondName.getText());
            prepStatementForAgent.setString(3, phone_no.getText());
            prepStatementForAgent.setString(4, email.getText());
            prepStatementForAgent.setString(5, address.getText());
            prepStatementForAgent.setString(6, postCode.getText());
            DateFormat df = new SimpleDateFormat("yyyy-mm-dd");
            prepStatementForAgent.setString(7, (String) df.format(dob.getDate()));
            boolean inserted = prepStatementForAgent.execute();
            if(!inserted){
                JOptionPane.showMessageDialog(null, "New agent added"); 
                setVisible(false);
                AddData addData = new AddData(userName);
                addData.setVisible(true);
            }else {
                JOptionPane.showMessageDialog(null, "Something went wrong. Please, try again"); 
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_addAgentActionPerformed

