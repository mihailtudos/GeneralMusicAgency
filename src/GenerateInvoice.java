    public void getDataAboutSelectedCompany(){
         String getCompanyDetailsSQL = "SELECT users.org_type, users.account_no, users.account_postcode, bookings.total FROM bookings INNER JOIN users ON bookings.user = users.id WHERE bookings.total > 0 AND users.user_level = \"corporation\"";
        try{
            companyDetails.setVisible(true);
            getOrganizationDetails = connection.prepareStatement(getCompanyDetailsSQL);
            result = getOrganizationDetails.executeQuery();
            while(result.next()){
                accountNumber.setText(result.getString("account_no"));
                orgType.setText(result.getString("org_type"));
                postCode.setText(result.getString("account_postcode"));
                total.setText("Â£" + result.getString("total"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
