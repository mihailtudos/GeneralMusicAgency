    private void addBandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBandActionPerformed
        // TODO add your handling code here:
        String addBandSQL =  "INSERT INTO `bands` (`name`, `bio`, `town`, `facebook`, `instagram`, `website`, `agent`) VALUES (?,?,?,?,?,?,?);";
        try{
            prepStatementForBand = connection.prepareStatement(addBandSQL);
            prepStatementForBand.setString(1, bandName.getText().toUpperCase());
            prepStatementForBand.setString(2, bandBio.getText());
            prepStatementForBand.setString(3, bandLocation.getText().toUpperCase());
            prepStatementForBand.setString(4, bandFacebook.getText());
            prepStatementForBand.setString(5, bandInstagram.getText());
            prepStatementForBand.setString(6, bandWebsite.getText());
            prepStatementForBand.setString(7, agent.getSelectedItem().toString().substring(0, 2));
            

            boolean count =prepStatementForBand.execute();
            if(!count){
                JOptionPane.showMessageDialog(null, "New band added");
                setVisible(false);
                AddData addData = new AddData(userName);
                addData.setVisible(true);
            }else {
                JOptionPane.showMessageDialog(null, "Something went wrong. Please, try again");
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_addBandActionPerformed

