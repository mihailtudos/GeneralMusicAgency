    private void fetchAgent(){
        String fetchAgentSQL = "SELECT id, CONCAT(first_name, ' ', second_name) AS agentName FROM agents";
        try{
            featchAgent = connection.prepareStatement(fetchAgentSQL);
            result = featchAgent.executeQuery();
            while(result.next()){
                agent.addItem(result.getString("id") + " " + result.getString("agentName"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
