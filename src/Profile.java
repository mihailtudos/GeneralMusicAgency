    private void securityDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_securityDetailsActionPerformed
        // TODO add your handling code here:
        String securitySQL = "UPDATE `users` SET password=?,  security_code =? WHERE user_name =?";
        try{
            securityStatement = connection.prepareStatement(securitySQL);
            
            
            
            if(Validation_and_Sanitization.checkPassword( userPassword.getText(),  userConfPassword.getText()).equals("-1")){
                userPassword.setText("");
                userConfPassword.setText("");
            } else {
                securityStatement.setString(1, Validation_and_Sanitization.checkPassword( userPassword.getText(),  userConfPassword.getText()));
                securityStatement.setString(2, userSecCode.getText());
                securityStatement.setString(3, userName);
                int i = securityStatement.executeUpdate();
                if(i >0){
                    JOptionPane.showMessageDialog(null, "Your changes were updated");
                    setVisible(false);
                    Profile profile = new Profile(userName);
                    profile.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null, "User Not Found. Try again!");
                }
            }
        } catch(Exception e){
            
        }
    }//GEN-LAST:event_securityDetailsActionPerformed

