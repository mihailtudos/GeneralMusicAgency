    public void fillCompany(){
         String getCompanySQL = "SELECT users.org_name FROM bookings INNER JOIN users ON bookings.user = users.id WHERE bookings.total > 0 AND users.user_level = \"corporation\"";
        try{
            getCompany = connection.prepareStatement(getCompanySQL);
            result = getCompany.executeQuery();
            while(result.next()){
                company.addItem(result.getString("org_name"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
